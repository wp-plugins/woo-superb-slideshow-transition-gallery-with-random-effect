<hr><h2>We can use this plug-in in Three different way.</h2>

<h2>1.Drag and drop the widget!</h2>
1.	Go to widget menu and drag and drop the "Woo superb slideshow transition gallery with random effect" widget to your sidebar location. or <br />

<h2>2.Paste the below code to your desired template location!</h2>
2.	Copy and past the below mentioned code to your desired template location(ie in PHP file).
<div style="padding-top:7px;padding-bottom:7px;">
<code style="padding:7px;">
&lt;?php if (function_exists (woo_show)) woo_show(); ?&gt;
</code></div>

<h2>3.Use below code in post or page!</h2>
 3. Use below code in post or page.
<div style="padding-top:7px;padding-bottom:7px;">
<input name="" style="width:400px;height:25px;" value="[woo-superb-slideshow=widget=YES]" type="text" />
</div>

<br />In above code "widget" is your gallery type, "YES" is random option.

